package com.example.esportdotaznik.respondent;

public class RespondentIdentificationForm {

    private String fromSchool = "";
    private String schoolIco = "";
    private String serviceMembership = "";
    private String sex = "";
    private String birthYear = "";
    private String gamerLevel = "";

    public String getFromSchool() {
        return fromSchool;
    }

    public void setFromSchool(String fromSchool) {
        this.fromSchool = fromSchool;
    }

    public String getSchoolIco() {
        return schoolIco;
    }

    public void setSchoolIco(String schoolIco) {
        this.schoolIco = schoolIco;
    }

    public String getServiceMembership() {
        return serviceMembership;
    }

    public void setServiceMembership(String serviceMembership) {
        this.serviceMembership = serviceMembership;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getBirthYear() {
        return birthYear;
    }

    public void setBirthYear(String birthYear) {
        this.birthYear = birthYear;
    }

    public String getGamerLevel() {
        return gamerLevel;
    }

    public void setGamerLevel(String gamerLevel) {
        this.gamerLevel = gamerLevel;
    }
}
